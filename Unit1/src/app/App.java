package app;

public class App {
    public static void main(String[] args) throws Exception {

        for (int i = 0; i <= 10; i = i +1) {
            System.out.println("The count is " + i);
            System.out.println("Let's keep counting.");
        }
        for (int f = 10; f <= 20; f = f +1) {
            System.out.println("The count is " + f);
            System.out.println("Let's keep counting.");
        }
        for (int r = 10; r <= 100; r = r +1) {
            System.out.println("The count is " + r);
            System.out.println("Let's keep counting.");
        }
        for (int c = 1000; c <= 256000; c = c +1) {
            System.out.println("The count is " + c);
            System.out.println("Let's keep counting.");
        }
        for (int b = 11; b <= 12; b = b +1) {
            System.out.println("The count is " + b);
            System.out.println("Let's keep counting.");
        }
        for (int t = 10; t <= 0; t = t -1) {
            System.out.println("The count is " + t);
            System.out.println("Let's keep counting.");
        }
    }
}
        
    
